"""Phase 2 card P2-12 gate for Linux shard summary aggregation.

This script merges summary artifacts generated by
`run_p2_linux_unified_execution_gate.py` and produces a single merged summary.
"""

from __future__ import annotations

import argparse
import json
import sys
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Iterable, Sequence


def discover_summary_files(
    *,
    summary_paths: Sequence[str],
    summary_globs: Sequence[str],
    artifacts_dir: str | None,
) -> list[Path]:
    discovered: dict[str, Path] = {}

    def _add(path: Path) -> None:
        resolved = path.resolve()
        discovered[str(resolved)] = resolved

    for item in summary_paths:
        _add(Path(item))

    for pattern in summary_globs:
        for candidate in sorted(Path.cwd().glob(pattern)):
            _add(candidate)

    if artifacts_dir:
        base = Path(artifacts_dir)
        for candidate in sorted(base.rglob("summary.json")):
            _add(candidate)

    if not discovered:
        fallback = Path(".claude/reports/linux_unified_gate/summary.json")
        if fallback.is_file():
            _add(fallback)

    return [discovered[key] for key in sorted(discovered.keys())]


def _coerce_int(value: Any, *, field: str, path: Path) -> int:
    if isinstance(value, bool) or not isinstance(value, int):
        raise ValueError(f"{path}: field '{field}' must be int")
    return value


def load_shard_summary(path: Path) -> dict[str, Any]:
    if not path.is_file():
        raise ValueError(f"{path}: summary file not found")

    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        raise ValueError(f"{path}: invalid JSON ({exc})") from exc

    if not isinstance(payload, dict):
        raise ValueError(f"{path}: summary payload must be object")

    required_fields = (
        "manifest_total_tests",
        "total_tests",
        "passed",
        "failed",
        "shard_total",
        "shard_index",
    )
    for field in required_fields:
        if field not in payload:
            raise ValueError(f"{path}: missing field '{field}'")

    manifest_total = _coerce_int(payload["manifest_total_tests"], field="manifest_total_tests", path=path)
    total_tests = _coerce_int(payload["total_tests"], field="total_tests", path=path)
    passed = _coerce_int(payload["passed"], field="passed", path=path)
    failed = _coerce_int(payload["failed"], field="failed", path=path)
    shard_total = _coerce_int(payload["shard_total"], field="shard_total", path=path)
    shard_index = _coerce_int(payload["shard_index"], field="shard_index", path=path)

    if manifest_total < 0:
        raise ValueError(f"{path}: manifest_total_tests must be >= 0")
    if total_tests < 0:
        raise ValueError(f"{path}: total_tests must be >= 0")
    if passed < 0 or failed < 0:
        raise ValueError(f"{path}: passed/failed must be >= 0")
    if passed + failed > total_tests:
        raise ValueError(f"{path}: passed + failed exceeds total_tests")
    if shard_total < 1:
        raise ValueError(f"{path}: shard_total must be >= 1")
    if shard_index < 1 or shard_index > shard_total:
        raise ValueError(f"{path}: shard_index out of range")

    return {
        "path": str(path),
        "manifest_total_tests": manifest_total,
        "total_tests": total_tests,
        "passed": passed,
        "failed": failed,
        "shard_total": shard_total,
        "shard_index": shard_index,
        "report_dir": str(payload.get("report_dir", "")),
    }


def aggregate_shard_summaries(summaries: Iterable[dict[str, Any]]) -> dict[str, Any]:
    items = list(summaries)
    if not items:
        raise ValueError("no shard summaries provided")

    structural_issues: list[str] = []
    manifest_totals = {item["manifest_total_tests"] for item in items}
    shard_totals = {item["shard_total"] for item in items}
    manifest_total_tests = min(manifest_totals)
    shard_total = max(shard_totals)

    if len(manifest_totals) > 1:
        structural_issues.append(
            f"manifest_total_tests mismatch across shards: {sorted(manifest_totals)}"
        )
    if len(shard_totals) > 1:
        structural_issues.append(f"shard_total mismatch across shards: {sorted(shard_totals)}")

    per_shard: dict[int, dict[str, Any]] = {}
    duplicate_shards: set[int] = set()
    for item in sorted(items, key=lambda value: (value["shard_index"], value["path"])):
        shard_index = item["shard_index"]
        if shard_index in per_shard:
            duplicate_shards.add(shard_index)
            continue
        per_shard[shard_index] = item

    if duplicate_shards:
        structural_issues.append(f"duplicate shard indexes: {sorted(duplicate_shards)}")

    expected_shards = set(range(1, shard_total + 1))
    present_shards = set(per_shard.keys())
    missing_shards = sorted(expected_shards - present_shards)
    if missing_shards:
        structural_issues.append(f"missing shard indexes: {missing_shards}")

    total_tests = sum(item["total_tests"] for item in per_shard.values())
    passed = sum(item["passed"] for item in per_shard.values())
    failed = sum(item["failed"] for item in per_shard.values())

    if total_tests != manifest_total_tests:
        structural_issues.append(
            f"aggregated total_tests ({total_tests}) != manifest_total_tests ({manifest_total_tests})"
        )

    overall_status = "passed" if failed == 0 and not structural_issues else "failed"

    return {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "manifest_total_tests": manifest_total_tests,
        "shard_total": shard_total,
        "shards_present": sorted(present_shards),
        "shards_missing": missing_shards,
        "totals": {
            "total_tests": total_tests,
            "passed": passed,
            "failed": failed,
        },
        "summary_files": [item["path"] for item in sorted(items, key=lambda value: value["path"])],
        "per_shard": [
            {
                "path": item["path"],
                "shard_index": item["shard_index"],
                "shard_total": item["shard_total"],
                "total_tests": item["total_tests"],
                "passed": item["passed"],
                "failed": item["failed"],
                "report_dir": item["report_dir"],
            }
            for item in sorted(per_shard.values(), key=lambda value: value["shard_index"])
        ],
        "structural_issues": structural_issues,
        "overall_status": overall_status,
    }


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Aggregate Linux shard summaries from P2 unified execution gate"
    )
    parser.add_argument(
        "--summary",
        action="append",
        default=[],
        help="Path to shard summary.json (repeatable)",
    )
    parser.add_argument(
        "--summary-glob",
        action="append",
        default=[],
        help="Glob pattern for summary files (repeatable)",
    )
    parser.add_argument(
        "--artifacts-dir",
        default=None,
        help="Directory to recursively scan for summary.json files",
    )
    parser.add_argument(
        "--output",
        default=".claude/reports/linux_unified_gate/merged_summary.json",
        help="Merged summary output file",
    )
    args = parser.parse_args()

    summary_files = discover_summary_files(
        summary_paths=args.summary,
        summary_globs=args.summary_glob,
        artifacts_dir=args.artifacts_dir,
    )
    if not summary_files:
        print("[p2-linux-shard-aggregation-gate] no summary files found", file=sys.stderr)
        return 2

    loaded: list[dict[str, Any]] = []
    try:
        for summary_file in summary_files:
            loaded.append(load_shard_summary(summary_file))
        merged = aggregate_shard_summaries(loaded)
    except ValueError as exc:
        print(f"[p2-linux-shard-aggregation-gate] {exc}", file=sys.stderr)
        return 2

    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(json.dumps(merged, indent=2, ensure_ascii=False), encoding="utf-8")

    print(f"merged summary: {output_path}")
    print(f"overall_status: {merged['overall_status']}")
    print(
        "totals: "
        f"{merged['totals']['passed']} passed, {merged['totals']['failed']} failed, "
        f"{merged['totals']['total_tests']} total"
    )
    if merged["structural_issues"]:
        print("structural issues:")
        for issue in merged["structural_issues"]:
            print(f"- {issue}")

    return 0 if merged["overall_status"] == "passed" else 1


if __name__ == "__main__":
    raise SystemExit(main())
